package au.com.isobar.screens.core.dto;

public class DepartList {

    public String departSydney;
    public String arriveMelbourne;
    public String flightCost;
    public String discountedCosts;

    public String getDepartSydney() {
        return departSydney;
    }

    public void setDepartSydney(String departSydney) {
        this.departSydney = departSydney;
    }

    public String getArriveMelbourne() {
        return arriveMelbourne;
    }

    public void setArriveMelbourne(String arriveMelbourne) {
        this.arriveMelbourne = arriveMelbourne;
    }

    public String getFlightcost() {
        return flightCost;
    }

    public void setFlightcost(String flightcost) {
        this.flightCost = flightcost;
    }

    public String getDiscountedCosts() {
        return discountedCosts;
    }

    public void setDiscountedCosts(String discountedCosts) {
        this.discountedCosts = discountedCosts;
    }


}
